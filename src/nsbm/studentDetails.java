    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String id = idtxt.getText();
        
        try {
            String sql = "Select * from student where StudentID = ?";
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/nsbm","root","");
        PreparedStatement stm = conn.prepareStatement(sql);
        stm.setObject(1, id);
        ResultSet rs = stm.executeQuery();
        while(rs.next()){
            nametxt.setText(rs.getString("StudentName"));
            nictxt.setText(rs.getString("Nic"));
            gendertxt.setText(rs.getString("Gender"));
            addresstxt.setText(rs.getString("Address"));
            telephonetxt.setText(rs.getString("Telephone"));
            emailtxt.setText(rs.getString("Email"));
            bdaytxt.setText(rs.getString("bday"));
        }
        }
        //catch (ClassNotFoundException ex) {
        //    JOptionPane.showMessageDialog(this, "No Driver found");
        //} 
        catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        char a = id.charAt(1);
        
        if(a == 'C'){
            schooltxt.setText("School of Computing");
        }
        else if (a == 'B'){
            schooltxt.setText("School of Business");
            
        }
        else{
            schooltxt.setText("Schol of Engineering");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

