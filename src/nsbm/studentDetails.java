    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        String name = nametxt.getText();
        String nic = nictxt.getText();
        String gender = gendertxt.getText();
        String address = addresstxt.getText();
        String telephone = telephonetxt.getText();
        String email = emailtxt.getText();
        String bday = bdaytxt.getText();
        updatedStudent stdnt = new updatedStudent(name, bday, nic, gender, address, telephone, email);
        
        
        
        
        try {
            int res = studentController.updateStudent(stdnt);
            if (res > 0) {
                JOptionPane.showMessageDialog(this, "Updated Successfully");
            }
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "No Driver found");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

